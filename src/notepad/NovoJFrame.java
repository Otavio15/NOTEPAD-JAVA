    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Font negrito = new Font("Negrito", Font.BOLD, numeroFont);
        String texto = editor.getText();
        String parteTexto = editor.getSelectedText();
        String resto = "";
        for (int i = 0; i < texto.length() - parteTexto.length(); i++) {
            resto += texto.charAt(i)+"";
        }
        editor2.setText(parteTexto);
        editor2.setFont(negrito);
        editor.setText(resto+editor2.getText());
    }//GEN-LAST:event_jButton1ActionPerformed

